    public static String Randomize(String[] arr) {
        int shuffle = new Random().nextInt(arr.length);
        /*Creates a new object "shuffle" from the Random class and it shuffles the 
        array elements and outputs a random word*/ 
        return arr[shuffle];
        
    }
