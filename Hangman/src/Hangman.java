    private void btnTryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTryActionPerformed
        try{
            char[] word = guessWord.toCharArray();
            char letter = txtWord.getText().toLowerCase().charAt(0);
            txtWord.requestFocus();//Gets focus on the textfield component for cursor to keep blinking in textfield
            
            int incrct = 0;
            for (int i = 0;i < word.length;i++) {
                
                if (word[i] == letter) {
                /*Replaces an X with the input character everytime a
                *correct letter is input
                */
                    switch (i) {
                        case 0:
                        wrdLbl1.setText(String.valueOf(letter));
                        break;
                        case 1:
                        wrdLbl2.setText(String.valueOf(letter));
                        break;
                        case 2:
                        wrdLbl3.setText(String.valueOf(letter));
                        break;
                        case 3:
                        wrdLbl4.setText(String.valueOf(letter));
                        break;
                        case 4:
                        wrdLbl5.setText(String.valueOf(letter));
                        break;
                        case 5:
                        wrdLbl6.setText(String.valueOf(letter));
                        break;
                        case 6:
                        wrdLbl7.setText(String.valueOf(letter));
                        break;
                        case 7:
                        wrdLbl8.setText(String.valueOf(letter));
                        break;
                        case 8:
                        wrdLbl9.setText(String.valueOf(letter));
                        break;
                        case 9:
                        wrdLbl10.setText(String.valueOf(letter));
                        break;
                        default:
                        break;
                    }
                    crctLetter++;
                }
                else {
                    incrct++;
                }

                if (incrct == word.length) {
                    incrct = 0;
                    incrctLetter++;
                    attemptMsgLbl.setText(Integer.toString(incrctLetter));
                    //Displays the no. of incorrect attempts in the label
                }

                txtWord.setText(null);//Makes textfield empty everytime the tryBtn is clicked            

                if (crctLetter == word.length) {
                    ImageIcon winimg = new ImageIcon(getClass().getResource("win1.gif"));
                    JOptionPane.showMessageDialog(null, "", "Congratulations! YOU WIN!", 
                            JOptionPane.INFORMATION_MESSAGE, winimg);
                    //Opens a JOptionPane and displays the GAME WON image
                    txtWord.setVisible(false);
                    /*Hides the textfield so that user cannot
                    enter more letters after the game is over*/

                }

                /*Updates the hangman button with a new image everytime an
                *incorrect character is entered
                */
                switch (incrctLetter) {
                    case 1:
                    hangBtn.setIcon(pic2);
                    break;
                    case 2:
                    hangBtn.setIcon(pic3);
                    break;
                    case 3:
                    hangBtn.setIcon(pic4);
                    break;
                    case 4:
                    hangBtn.setIcon(pic5);
                    break;
                    case 5:
                    hangBtn.setIcon(pic8);
                    break;                
                    case 6:
                    hangBtn.setIcon(pic9);
                    ImageIcon loseimg = new ImageIcon(getClass().getResource("lose.gif"));
                    JOptionPane.showMessageDialog(null, "The correct word was " +"\"" + 
                        guessWord + "\"", "GAME OVER!", JOptionPane.INFORMATION_MESSAGE, loseimg);
                    //Opens a JOptionPane and displays the GAME LOST image
                    txtWord.setVisible(false);
                    break;                    
                }
            }
        }
        //Catches the exception that occurs when the try button is clicked without entering a character
        catch(StringIndexOutOfBoundsException e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnTryActionPerformed
    
